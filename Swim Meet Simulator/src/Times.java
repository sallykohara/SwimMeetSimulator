
public class Times
	{
			private String name; 
			private int seconds;
			
			public Times(String n, int s)
			{
				name = n;
				seconds = s;
			}
			public String getName()
			{
				return name;
			}
			public void setName(String n)
			{
				name = n;
			}
			public int getSeconds()
			{
				return seconds;
			}
			public void setSeconds(int s)
			{
				seconds = s;
			}			
		}
	
